    private boolean isFieldsCorrect() {
        return !nameField.getText().equals("") &&
                !descriptionField.getText().equals("") &&
                !tagsField.getText().equals("") &&
                !costField.getText().equals("") &&
                costField.getText().matches("\\d+") &&
                !sourcePathField.getText().equals("") &&
                !testDataPathField.getText().equals("");
    }

    private void onOK() {
        if (isFieldsCorrect()) {
            try {
                byte[] srcBytes = null;
                byte[] testBytes = null;
                File srcFile = new File(sourcePathField.getText());
                File testFile = new File(testDataPathField.getText());
                if (!srcFile.exists()) {
                    statusField.setText("Source file not exists.");
                    return;
                } else if (!testFile.exists()) {
                    statusField.setText("Test file not exists.");
                    return;
                } else {
                    srcBytes = Files.readAllBytes(Paths.get(sourcePathField.getText()));
                    testBytes = Files.readAllBytes(Paths.get(testDataPathField.getText()));

                    Algorithm algorithm = new Algorithm(
                            nameField.getText(),
                            descriptionField.getText(),
                            new ArrayList<>(Arrays.asList(tagsField.getText().split(" "))),
                            srcBytes, testBytes, client.getUserName(), Integer.parseInt(costField.getText()),
                            (Language) languageComboBox.getSelectedItem()
                    );
                    client.setAlgorithmToAdd(algorithm);
                    client.addAlgorithm();
                }
            } catch (Exception ex) {
                statusField.setText(ex.toString());
                return;
            }

            dispose();
        }
        statusField.setText("Some fields are not correct.");
    }

